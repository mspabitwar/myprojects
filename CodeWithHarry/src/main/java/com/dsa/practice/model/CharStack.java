package com.dsa.practice.model;

public class CharStack {

	int size;
	int top;
	char[] array;

	public int getSize() {
		return size;
	}

	public void setSize(int size) {
		this.size = size;
	}

	public int getTop() {
		return top;
	}

	public void setTop(int top) {
		this.top = top;
	}

	public char[] getArray() {
		return array;
	}

	public void setArray(char[] array) {
		this.array = array;
	}
}